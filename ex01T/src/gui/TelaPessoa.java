    private void jButtonExecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExecutarActionPerformed
        // TODO add your handling code here:
        try {
            //CRIANDO OBJETO
            Pessoa person = new Pessoa();

            //ENTRADA DE DADOS
            person.setNome(jTextFieldNome.getText());
            person.setPeso(Integer.parseInt(jTextFieldPeso.getText()));
            person.setAltura(Float.parseFloat(jTextFieldAltura.getText()));

            //Sa√≠da de Dados
            String output = "DADOS DA PESSOA\n"
                    + "Nome: " + person.getNome() + "\n"
                    + "Peso: " + person.getPeso() + "\n"
                    + "Altura: " + person.getAltura() + "\n"
                    + "Calculo do IMC: " + person.calcularIMC() + "\n";

            jTextAreaOutput.setText(output);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO GRANDE NA TELA!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonExecutarActionPerformed

